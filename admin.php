    <div><h3>Sta. Maria</h3>
      <p style="text-align: justify;">Santa Maria, officially the Municipality of Santa Maria, is a 3rd class municipality in the province of Ilocos Sur, Philippines. According to the 2020 census, it has a population of 30,006 people.<br>
      Situated at the western coastline of the island of Luzon, Santa Maria is nestled in a valley surrounded by mountains. It lies in the central part of the province of Ilocos Sur. Santa Maria is 369 kilometres from Metro Manila and 38 kilometres from Vigan City, the provincial capital.</p>
    </div>
