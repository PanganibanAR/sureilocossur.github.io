    <div><h3>Narvacan</h3>
       <p style="text-align: justify;">Narvacan, officially the Municipality of Narvacan, is a 2nd class municipality in the province of Ilocos Sur, Philippines.<br>

      Narvacan was organized under the traditions of the royal government of Spain. The Habsburg royal family served as the heads of state which in turn appointed Santiago de Vera as President of the Royal Audiencia-governor over the region in which Narvacan was situated. In 1589, Governor Vera appointed Nicolas de Figueroa as the first Encomendero de Narvacan-principal administrator of the town and its neighbors in the encomienda system. His role eventually evolved into the office of alcalde</p>
    </div></div></div>
