      <p style="text-align: justify;">Santiago is a coastal municipality in the province of Ilocos Sur.<br>

      The municipality has a land area of 46.36 square kilometers or 17.90 square miles which constitutes 1.79% of Ilocos Sur's total area. Its population as determined by the 2020 Census was 19,471. This represented 2.76% of the total population of Ilocos Sur province, or 0.37% of the overall population of the Ilocos Region. Based on these figures, the population density is computed at 420 inhabitants per square kilometer or 1,088 inhabitants per square mile.</p>
