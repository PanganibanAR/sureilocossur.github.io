    <div><h3>Candon City</h3>
       <p style="text-align: justify;">This once small resort town is known for making the heaviest and largest kalamay, a sweet and sticky snack made from sticky rice, coconut milk and sugar, in the world. This city also has a rich historical background. In its legends, the name of the city is derived from the legendary "kandong" tree which is now but extinct in the area. Its patron saint is John of Sahagun and his feast day is celebrated every June 12.<br></p>
  </div></div>
