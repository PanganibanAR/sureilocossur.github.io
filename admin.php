    <h3>Vigan City</h3>
    <p style="text-align: justify;">Located on the western coast of the large island of Luzon, facing the South China Sea, it is a UNESCO World Heritage Site and it is one of the few Spanish colonial towns left in the Philippines whose old structures have mostly remained intact, and it is well known for its sett pavements and a unique architecture of the Spanish Philippines colonial era which fuses Native Philippine and Oriental building designs and construction, with colonial Spanish architecture that is still abundant in the area, mainly the Bahay na Bato houses and an Earthquake Baroque church.</p>
